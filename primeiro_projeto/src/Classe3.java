

public class Classe3 {
    /* Faça uma soma com valores int e float e imprima a suas médias
    */ 
    public static void main(String[] args){
       //Atribuindo valores
       int numero1 = 5;
       int numero2 = 4;
       
        //calculando a média e imprimindo ela no console
        int media = ((numero1 + numero2) / 2);
        System.out.println("A media eh: " + media);
        
        //Atribuindo valores de float
        double numero3 = 3.0;
        double numero4 = 4.0;
        
        //Calculando a média e imprimindo ela no console
        double media2 = ((numero3 + numero4) / 2);
        System.out.println("A media eh :" + media2);
    }
}
